#include <RcppArmadillo.h>
// [[Rcpp::depends(RcppArmadillo)]]
using namespace arma;

//[[Rcpp::export]]
Rcpp::List demo_inits(){
  vec v0(10);
  mat m0(2,3);
  vec V0 = zeros<vec>(10);
  mat M0 = zeros<mat>(2,3);
  vec V1 = ones<vec>(10);
  mat M1 = ones<mat>(2,3);
  vec VU = randu<vec>(5);
  mat MU = randu<mat>(2,3);
  vec VN = randn<vec>(10);
  mat MN = randn<mat>(2,3);
  
  return Rcpp::List::create(
    Rcpp::Named("v0") = v0, Rcpp::Named("m0") = m0,
    Rcpp::Named("V0") = V0, Rcpp::Named("M0") = M0,
    Rcpp::Named("V1") = V1, Rcpp::Named("M1") = M1,
    Rcpp::Named("VU") = VU, Rcpp::Named("MU") = MU,
    Rcpp::Named("VN") = VN, Rcpp::Named("MN") = MN
  );
}

